<script>
    document.addEventListener('DOMContentLoaded', function() {
        const carouselElement = document.querySelector('#leadershipCarousel');
        const prevBtn = document.getElementById('prevSlide');
        const nextBtn = document.getElementById('nextSlide');
        const teamSection = document.getElementById('team'); // القسم اللي بدنا نرجعله

        if (!carouselElement || !bootstrap?.Carousel) {
            console.error('❌ Carousel element or Bootstrap not ready');
            return;
        }

        // تهيئة الكاروسيل مع اللمس
        let carousel = bootstrap.Carousel.getInstance(carouselElement);
        if (!carousel) {
            carousel = new bootstrap.Carousel(carouselElement, {
                interval: false,
                ride: false,
                pause: true,
                wrap: true,
                touch: true
            });
        }

        carousel.cycle = function() {};
        carousel.pause();

        // عند الضغط على السهم التالي
        nextBtn?.addEventListener('click', () => {
            carousel.next();
            if (teamSection) {
                teamSection.scrollIntoView({
                    behavior: 'smooth'
                });
            }
        });

        // عند الضغط على السهم السابق
        prevBtn?.addEventListener('click', () => {
            carousel.prev();
            if (teamSection) {
                teamSection.scrollIntoView({
                    behavior: 'smooth'
                });
            }
        });
    });
</script>
