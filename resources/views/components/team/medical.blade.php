<script>
    document.addEventListener('DOMContentLoaded', function() {
        const carouselElement = document.querySelector('#medicalCarousel');
        const medicalSection = document.getElementById('medical');

        if (!carouselElement || !bootstrap?.Carousel || !medicalSection) {
            console.error('❌ Medical carousel or target section not found');
            return;
        }

        const carousel = bootstrap.Carousel.getInstance(carouselElement) ??
            new bootstrap.Carousel(carouselElement, {
                interval: false,
                ride: false,
                pause: true,
                wrap: true,
                touch: true // ✅ تفعيل اللمس
            });

        carousel.cycle = function() {};
        carousel.pause();

        // ✅ على كل انتقال، نرجع المستخدم لقسم medical
        carouselElement.addEventListener('slid.bs.carousel', () => {
            if (window.innerWidth <= 768) {
                // يرجع المستخدم لقسم medical
                medicalSection.scrollIntoView({
                    behavior: 'smooth'
                });
            }
        });
    });
</script>
